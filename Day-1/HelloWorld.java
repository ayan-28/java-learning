public class HelloWorld {

    /**
     * main() function executes when Java program is run.
     * 
     * public (access modifier)  	= accessible to all 
     * static (keyword) 			= object does not need to be declared to be used
     * void   (return type) 		= returns nothing
	 * main   (method name) 		= this method name is searched by JVM as a starting point for an application
	 * String[] args (parameter to the main method)
     */
    public static void main(String[] args) {
        // printing hello world! (Single line comment)
        System.out.println("Hello, World!");
    }
}
